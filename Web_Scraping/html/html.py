# HTML 주요 태그

# 태그는 속성(attribute)이라는 추가 정보 보유 가능

# HTML 주요 태그 리스트
# html : HTML 문서
# head : 문서 헤더
# title : 문서 제목
# body(속성 : text, background, bgcolor 등) : 문서 바디
# h1, h2, h3 : 머리말(heading)
# p : 문단(paragraph)
# br : 줄바꿈(line break)
# b : 볼드체
# i : 이탤릭체
# s : 취소선(strike)
# ol, ul(속성 : type 등) : 순서 리스트, 비순서 리스트
# li(속성 : type 등) : 리스트 아이템
# table(속성 : width, border, cellpadding) : 테이블
# tr(속성 : align 등) : 테이블 행
# th, td(속성 : colspan 등) : 테이블 헤더, 테이블 데이터
# a(속성 : href, target 등) : 하이퍼링크
# img(속성 : src, width, height) : 이미지
# <!--...--> : 주석

